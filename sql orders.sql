CREATE TABLE IF NOT EXISTS TABLE_ORDERS (
  'PRO_ID' INTEGER PRIMARY KEY AUTOINCREMENT,
  'SUPPLIER_NAME' TEXT NOT NULL,
  'PRODUCT_NAME' TEXT NOT NULL,
  'CATEGORY' INTEGER NOT NULL,
  'PRICE' REAL NOT NULL,
  'ORDER_DATE' TEXT NOT NULL,
  'UNIT' TEXT NOT NULL
);

